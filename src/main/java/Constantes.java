    public class Constantes {
        public static void main(String[] args) {
            /*
            Una variable es un elemento de datos cuyo valor
            puede cambiar durante el curso de la ejecuci√≥n de un programa
            */
            int numero = 10;
            System.out.println(numero);
            numero = 15;
            System.out.println(numero);
            // Una constante es un espacio en memoria donde almancena un valor
            //No cambia a lo largo de la ejecucion de un programa
            final int numero1 = 5;              //Se declara poniendo la palabra "final"
            System.out.println(numero1);

        }
    }
