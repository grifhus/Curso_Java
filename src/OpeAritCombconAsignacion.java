public class OpeAritCombconAsignacion {
    public static void main(String[] args) {
        int numero = 10;
        numero = numero + 5;
        numero += 5;                   // numero += 5 es igual a numero = numero + 5
        numero -= 5;
        numero *= 5;
        numero /= 5;
        numero %= 5;
        System.out.println(numero);
    }
}
